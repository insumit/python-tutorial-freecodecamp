# Working with strings in python
# Simple String
print("Hello World")
# New line inside a string
print("Hello\nWorld")
# Quotation mark
print("Hello\"World")
# String variable
phrase = "New Delhi"
print(phrase + " is cool")
# String functions
print(phrase.lower())
print(phrase.upper())
print(phrase.isupper())
# Function chaining
print(phrase.upper().isupper())
print(len(phrase))
print(phrase[2])
print(phrase.index("Del"))
print(phrase.replace("Delhi", "Elephant"))
